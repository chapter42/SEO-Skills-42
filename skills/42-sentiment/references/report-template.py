def slugify(text: str) -> str:
    """Generate URL-safe slug from text."""
    slug = text.lower().strip()
    slug = re.sub(r"[^a-z0-9\s-]", "", slug)
    slug = re.sub(r"[\s]+", "-", slug)
    slug = re.sub(r"-+", "-", slug).strip("-")
    return slug[:30]


def load_font_base64(font_path: Path) -> str:
    """Read font file and return base64-encoded string."""
    with open(font_path, "rb") as f:
        return base64.b64encode(f.read()).decode("ascii")


def load_svg(svg_path: Path) -> str:
    """Read SVG file and return contents for inline embedding."""
    with open(svg_path, "r", encoding="utf-8") as f:
        return f.read()


def esc(text) -> str:
    """HTML-escape text safely, handling None."""
    if text is None:
        return ""
    return html.escape(str(text))


def confidence_display(conf: str, lang: str) -> str:
    """Translate confidence level."""
    labels = {
        "nl": {"high": "Hoog", "medium": "Gemiddeld", "low": "Laag"},
        "en": {"high": "High", "medium": "Medium", "low": "Low"},
    }
    return labels.get(lang, labels["en"]).get(conf, conf)


def lean_display(lean: str, lang: str) -> str:
    """Translate sentiment lean."""
    labels = {
        "nl": {"positive": "positief", "negative": "negatief", "neutral": "neutraal", "mixed": "gemengd"},
        "en": {"positive": "positive", "negative": "negative", "neutral": "neutral", "mixed": "mixed"},
    }
    return labels.get(lang, labels["en"]).get(lean, lean)


