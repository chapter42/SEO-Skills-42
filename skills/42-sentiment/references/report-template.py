def main():
    parser = argparse.ArgumentParser(
        description="Generate HTML and Markdown sentiment reports from AnalysisResult JSON.",
    )
    parser.add_argument(
        "--brand-dir",
        required=True,
        help="Path to brand-assets/ directory containing fonts/ and logo-svg/",
    )
    parser.add_argument(
        "--output-dir",
        required=True,
        help="Path to output directory for generated reports",
    )
    args = parser.parse_args()

    # Read JSON from stdin
    try:
        raw = sys.stdin.read()
        data = json.loads(raw)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON input: {e}", file=sys.stderr)
        sys.exit(1)

    # Validate required keys
    missing = [k for k in REQUIRED_KEYS if k not in data]
    if missing:
        print(f"Error: Missing required keys in input: {', '.join(missing)}", file=sys.stderr)
        sys.exit(1)

    # Resolve paths
    brand_dir = Path(args.brand_dir).resolve()
    output_dir = Path(args.output_dir).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    # Generate filenames
    slug = slugify(data["query"])
    datum = date.today().strftime("%Y-%m-%d")
    html_path = output_dir / f"{slug}-{datum}.html"
    md_path = output_dir / f"{slug}-{datum}.md"

    # Generate reports
    generate_html(data, brand_dir, html_path)
    generate_markdown(data, md_path)

    # Output paths
    print(str(html_path))
    print(str(md_path))


if __name__ == "__main__":
    main()
