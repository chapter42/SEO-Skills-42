def generate_markdown(data: dict, output_path: Path) -> None:
    """Generate structured Markdown report from AnalysisResult."""
    lang = data.get("lang", "nl")
    tr = TR.get(lang, TR["nl"])
    datum = date.today().strftime("%Y-%m-%d")
    period_label = PERIOD_LABELS.get(lang, PERIOD_LABELS["nl"]).get(data["period"], data["period"])

    lines = []

    # Title and meta
    lines.append(f"# {tr['title_prefix']}: {data['query']}")
    lines.append("")
    lines.append(f"**{tr['period_label_text']}:** {period_label} | **{tr['source']}:** {data['total_items']} {tr['sources_count']} {data['total_sources']} {tr['platforms']} | **{tr['date_label']}:** {datum}")
    lines.append("")

    # Executive summary
    lines.append(f"## {tr['summary_heading']}")
    lines.append("")
    lines.append(data["executive_summary"])
    lines.append("")

    # Overall sentiment
    s = data["overall_sentiment"]
    lines.append(f"## {tr['overall_heading']}")
    lines.append("")
    lines.append(f"| {tr['positive']} | {tr['negative']} | {tr['neutral']} |")
    lines.append("|----------|----------|----------|")
    lines.append(f"| {s['positive']}% | {s['negative']}% | {s['neutral']}% |")
    lines.append("")

    # Per-source sentiment
    lines.append(f"## {tr['sources_heading']}")
    lines.append("")
    lines.append(f"| {tr['source']} | {tr['positive']} | {tr['negative']} | {tr['neutral']} | {tr['items']} | {tr['confidence']} |")
    lines.append("|------|----------|----------|----------|-------|------|")
    for src in data["per_source_sentiment"]:
        ss = src["sentiment"]
        conf = confidence_display(src["confidence"], lang)
        lines.append(f"| {src['source_label']} | {ss['positive']}% | {ss['negative']}% | {ss['neutral']}% | {src['item_count']} | {conf} |")
    lines.append("")

    # Emotion profile
    lines.append(f"## {tr['emotions_heading']}")
    lines.append("")

    # Core emotions (skip table if empty)
    core_emotions = data.get("emotion_profile", {}).get("core_emotions", [])
    if core_emotions:
        lines.append(f"### {tr['core_emotions']}")
        lines.append("")
        lines.append(f"| {tr['emotion']} | {tr['percentage']} |")
        lines.append("|--------|------------|")
        for e in core_emotions:
            lines.append(f"| {e['label']} | {e['percentage']}% |")
        lines.append("")

    # Dynamic emotions
    dynamic = data.get("emotion_profile", {}).get("dynamic_emotions", [])
    if dynamic:
        lines.append(f"### {tr['dynamic_emotions']}")
        lines.append("")
        lines.append(f"| {tr['emotion']} | {tr['percentage']} |")
        lines.append("|--------|------------|")
        for e in dynamic:
            lines.append(f"| {e['label']} | {e['percentage']}% |")
        lines.append("")

    # Themes & Topics
    lines.append(f"## {tr['themes_heading']}")
    lines.append("")

    for tg in data.get("theme_groups", []):
        lines.append(f"### {tg['theme']}")
        lines.append("")

        for topic in tg.get("topics", []):
            lines.append(f"#### {topic['name']}")
            lines.append("")

            ts = topic["sentiment"]
            lines.append(f"**{tr['sentiment_label']}:** {ts['positive']}% {tr['positive'].lower()} / {ts['negative']}% {tr['negative'].lower()} / {ts['neutral']}% {tr['neutral'].lower()}")
            lines.append("")
            lines.append(topic.get("explanation", ""))
            lines.append("")

            # Dominant emotions
            dom = topic.get("dominant_emotions", [])
            if dom:
                labels = ", ".join(e.get("label", e.get("name", "")) for e in dom)
                lines.append(f"**{tr['dominant_emotions']}:** {labels}")
                lines.append("")

            # Per-source leans
            ps_list = topic.get("per_source", [])
            if ps_list:
                leans = ", ".join(
                    f"{ps['source_label']} ({lean_display(ps.get('sentiment_lean', ''), lang)})"
                    for ps in ps_list
                )
                lines.append(f"**{tr['per_source_leans']}:** {leans}")
                lines.append("")

            # Sarcasm note
            sarcasm = topic.get("sarcasm_note")
            if sarcasm:
                lines.append(f"*{tr['sarcasm_note_label']}: {sarcasm}*")
                lines.append("")

            # Quotes
            for q in topic.get("quotes", []):
                lines.append(f"> {q.get('text', '')}")
                lines.append(">")
                lines.append(f"> -- [{q.get('source_label', '')}]({q.get('url', '')})")
                lines.append("")

    # Methodology
    lines.append(f"## {tr['methodology_heading']}")
    lines.append("")
    methodology_text = tr["methodology_text"].format(
        datum=datum,
        reasoning=data["confidence_assessment"].get("reasoning", ""),
    )
    lines.append(methodology_text)
    lines.append("")

    quality_notes = data["confidence_assessment"].get("source_quality_notes", [])
    if quality_notes:
        for note in quality_notes:
            lines.append(f"- {note}")
        lines.append("")

    # Footer
    lines.append("---")
    lines.append(f"*{tr['footer'].format(datum=datum)}*")
    lines.append("")

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


