def _inject_js_functions(config_str: str) -> str:
    """Replace placeholder strings with raw JS functions."""
    replacements = {
        '"__FUNC_ABS_PERCENT__"': "function(v) { return Math.abs(v) + '%'; }",
        '"__FUNC_TOOLTIP_PCT__"': "function(ctx) { return ctx.dataset.label + ': ' + ctx.raw + '%'; }",
        '"__FUNC_TOOLTIP_ABS_PCT__"': "function(ctx) { return ctx.dataset.label + ': ' + Math.abs(ctx.raw) + '%'; }",
        '"__FUNC_PCT_TICK__"': "function(v) { return v + '%'; }",
    }
    for placeholder, func in replacements.items():
        config_str = config_str.replace(placeholder, func)
    return config_str


